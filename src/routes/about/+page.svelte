	<br><br>
	<strong>Purpose of the research</strong> <br>The aim of the study is to examine the hierarchical representation of different themes in chess by segmenting events.
	It is conducted as part of scientific research carried out by the Department of Cognitive Science and Psychology. The study has received approval from the Committee for Research Ethics, Department of Cognitive Science and Psychology, New Bulgarian University. 
	<br><br>
	<strong>Procedure</strong><br>
	You will have two training sessions before participating in the study. During both the training and study, you will watch chess game sequences on the screen. Your task will be to press a red button below the chessboard to identify a meaningful event. Your goal will be to segment the moves into the largest or smallest natural and meaningful units based on your understanding of the game. 
	<br><br>
	<strong>Duration</strong> <br>
35 - 40 Minutes<br><br>
<strong>Voluntary Participation</strong> <br>
Your participation is on an entirely voluntary basis, it comes with no known risks and without any form of compensation. You are free to withdraw your participation at any time and without giving a reason. Withdrawing from this study has absolutely no consequences. <br><br>
<strong>Confidentiality</strong><br>
You'll be asked for personal info (chess ranking, gender, age). Data security via Google Firebase Services will be used, ensuring encrypted storage and access controls to protect against unauthorized access and breaches. Your anonymity is guaranteed throughout the study.
